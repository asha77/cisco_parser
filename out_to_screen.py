def print_devices_summary(devices):
    tbl_header_out2scr()

    i = 1
    for dev in devices:
        print('| {0:4d} | {1:75s} | {2:25s} | {3:15s} | {4:15s} | {5:20s} | {6:18s} | {7:24s} | {8:10s} | {9:12s} | {10:12s} |'.format(
        i,
        dev['config_filename'],
        dev['hostname'],
        dev['mgmt_ipv4_from_filename'],
        dev['mgmt_v4_autodetect'],
        dev['domain_name'],
        dev['family'],
        dev['model'],
        dev['serial'],
        dev['os'],
        dev['sw_version'])
    )

    # завершаем таблицу
    tbl_footer_out2scr()
    return True